'use strict';

const fs = require('node:fs');
const path = require('node:path');
const esbuild = require('esbuild');
const entries = require('./dashboard-css-sources.json');
const { writeFileAtomic } = require('./core/atomic-file');

const srcDir = __dirname;
const outFile = path.join(srcDir, 'dashboard-bundle.css');

function build() {
  if (!Array.isArray(entries) || !entries.length) throw new Error('Dashboard CSS source manifest is empty');
  const source = entries.map((name) => {
    const file = path.join(srcDir, name);
    if (!fs.existsSync(file)) throw new Error(`Missing dashboard stylesheet: ${name}`);
    return `/* source: ${name} */\n${fs.readFileSync(file, 'utf8')}`;
  }).join('\n');
  const result = esbuild.transformSync(source, {
    loader: 'css',
    minify: true,
    legalComments: 'none',
    charset: 'utf8',
  });
  writeFileAtomic(outFile, `/* Generated by src/build-dashboard-css.js from ${entries.length} ordered sources. */\n${result.code.trim()}\n`);
  const sourceKb = (Buffer.byteLength(source) / 1024).toFixed(1);
  const bundleKb = (fs.statSync(outFile).size / 1024).toFixed(1);
  console.log(`dashboard css bundle: ${path.relative(path.dirname(srcDir), outFile)} ${bundleKb}KB (${entries.length} files, ${sourceKb}KB source)`);
}

if (require.main === module) build();
module.exports = { build };
