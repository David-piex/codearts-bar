'use strict';
const assert=require('node:assert/strict');
const fs=require('node:fs');
const os=require('node:os');
const path=require('node:path');
const {spawnSync}=require('node:child_process');
const root=path.resolve(__dirname,'..');
const pkg=require('../package.json');
const runtime=path.join(root,'.cache','cli-runtime');
const manifest=JSON.parse(fs.readFileSync(path.join(runtime,'CLI_RUNTIME_MANIFEST.json'),'utf8'));
assert.equal(manifest.entry,'src/bin.js');
assert.ok(manifest.files.includes('src/bin.js'));
assert.equal(fs.existsSync(path.join(runtime,'src','main.js')),false);
const npmStage=path.join(root,'.cache','npm-package');
assert.equal(fs.existsSync(path.join(npmStage,'src','bin.js')),true);
assert.equal(fs.existsSync(path.join(npmStage,'src','main.js')),false);
assert.equal(fs.existsSync(path.join(npmStage,'src','dashboard-renderer.js')),false);
const npmPkg=JSON.parse(fs.readFileSync(path.join(npmStage,'package.json'),'utf8'));
assert.equal(npmPkg.version,pkg.version);
assert.equal(npmPkg.bin['codearts-bar'],'src/bin.js');
const run=spawnSync(process.execPath,[path.join(npmStage,'src','bin.js'),'self-test'],{cwd:npmStage,encoding:'utf8',timeout:30000});
assert.equal(run.status,0,run.stderr||run.stdout);
console.log(`ok - npm package staging files=${manifest.files.length}`);
